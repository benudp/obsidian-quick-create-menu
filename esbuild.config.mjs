import fs from "fs";
import path from "path";
import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { readFile, cp, mkdir } from "fs/promises";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
*/
`;

const prod = process.argv[2] === "production";

/* ---------------- helper ---------------- */

async function getObsidianDir() {
  try {
    const env = JSON.parse(await readFile("env.json", "utf8"));
    if (!env.OBSIDIAN_PLUGIN_DIR)
      throw new Error("OBSIDIAN_PLUGIN_DIR missing");
    return env.OBSIDIAN_PLUGIN_DIR;
  } catch (e) {
    console.error(
      "\x1b[31m%s\x1b[0m",
      "env.json missing or invalid. Sync disabled.",
    );
    return null;
  }
}

function renameCSS() {
  const from = path.resolve("dist/main.css");
  const to = path.resolve("dist/styles.css");
  if (fs.existsSync(from)) {
    fs.renameSync(from, to);
  }
}

/* ---------------- plugins ---------------- */

const copyManifestPlugin = {
  name: "copy-manifest",
  setup(build) {
    build.onEnd(async (result) => {
      if (result.errors.length > 0) return;
      if (!fs.existsSync("dist")) await mkdir("dist");
      await cp("manifest.json", "dist/manifest.json");
    });
  },
};

const syncToObsidianPlugin = (obsidianDir) => ({
  name: "sync-to-obsidian",
  setup(build) {
    build.onEnd(async (result) => {
      if (result.errors.length > 0 || !obsidianDir) return;

      if (!fs.existsSync(obsidianDir)) {
        await mkdir(obsidianDir, { recursive: true });
      }

      renameCSS();

      const files = [
        ["dist/main.js", "main.js"],
        ["dist/styles.css", "styles.css"],
        ["dist/manifest.json", "manifest.json"],
      ];

      for (const [from, to] of files) {
        const src = path.resolve(from);
        const dest = path.join(obsidianDir, to);
        if (fs.existsSync(src)) {
          await cp(src, dest);
        }
      }

      console.log("\x1b[32m%s\x1b[0m", `✔ Synced to Obsidian: ${obsidianDir}`);
    });
  },
});

/* ---------------- build ---------------- */

const obsidianDir = await getObsidianDir();

const context = await esbuild.context({
  banner: { js: banner },
  entryPoints: ["src/main.ts"],
  bundle: true,
  external: ["obsidian", "electron", ...builtins],
  format: "cjs",
  target: "es2018",
  logLevel: "info",
  sourcemap: prod ? false : "inline",
  treeShaking: true,
  outdir: "dist",
  minify: prod,
  loader: {
    ".css": "css",
  },

  plugins: [copyManifestPlugin, syncToObsidianPlugin(obsidianDir)],
});

if (prod) {
  await context.rebuild();
  await context.dispose();
} else {
  await context.watch();
}
